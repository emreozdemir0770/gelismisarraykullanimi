#dizileri oluşturma
a=["emre","mehmet","okan","barcın"]



#yazdırma 
print(a)


# tipini yazdırma 
print(type(a))


#girilen dizi sırasındaki kişiyi yazdırır  0 dan başlar
print(a[1])


#♥başlama ve bitiş çizgisini veriyor.
print(a[0:3])


#bitiş çizgisini veriyor.
print(a[:5])


#başlama çizgisini veriyor.sonuna kadar devam eder
print(a[3:])


#aralıklarlar yazdırma  (3'er 3'er yadırma)
print(a[0::3])



# 1 den başlayarak dizi sayısını verir
print(len(a))